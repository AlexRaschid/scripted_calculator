// Takes X and divides it by Y
function divide(x,y)
{
  return x/y;
}

// Takes X and multiplies it by Y

function multiply(x,y){
    return x*y;
}

// Takes X and subtracts Y from it

function subtract(x,y){
    return x-y;
}

//  Takes X and adds Y to it
function add(x,y){
    return x+y;
    
}

// Takes X and raises it to the power of Y

function power(x,y){
    return Math.pow(x,y)
}

// Takes X and put it in square root form

function square_root(x){
    return Math.sqrt(x);
}

